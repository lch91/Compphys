/*
** Constants
*/

#include "constants.h"

// Mathematical
const double cPI   = 3.141592653589793; // PI

// Physical
const double cC    = 2.99792458e8;      // Speed of light in vacuum (defined) [m/s]
const double cG    = 6.67428e-11;       // Gravity                            [m^3/kg*s^2]
const double cH    = 6.62606896e-34;    // Planck's constant                  [j*s]
const double cHBar = 1.054571628e-34;   // Reduced Planck's constant          [j*s]
const double cMu0  = 4*cPI*10e-7;       // Magnetic constant (defined)        [N/A^2]
const double cEps0 = 8.854187817e-12;   // Electric constant (defined)        [F/m]
const double cKe   = 1/(4*cPI*cEps0);   // Coulomb's constant (defined)       [N*m^2/C]
const double cE    = 1.602176487e-19;   // Elementary charge                  [C]
const double cU    = 1.66053886e-27;    // Atomic mass unit                   [kg]
const double cNA   = 6.0221415e23;      // Avogadro's number                  [1/mol]
const double cK    = 1.3806504e-23;     // Boltzmann's constant               [j/K]
const double cEV   = 1.602176487e-19;   // Electronvolt                       [j]

// Natural Units
const double cPL   = 1.616252e-35;      // Planck length                      [m]
const double cPM   = 2.17644e-8;        // Planck mass                        [kg]
const double cPTm  = 5.39124e-44;       // Planck time                        [s]
const double cPQ   = 1.875545870e-18;   // Planck charge                      [C]
const double cPTp  = 1.416785e32;       // Planck temperature                 [K]

// Astronomical Units
const double cAU   = 1.496e11;          // Astronomical Unit                  [m]
const double cYr   = 365.2421897*86400; // Tropical year                      [s]
